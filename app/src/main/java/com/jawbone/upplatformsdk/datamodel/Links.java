package com.jawbone.upplatformsdk.datamodel;

public class Links {
    public String next;
    public String prev;

    public String getNext() {
        return this.next;
    }

    public void setNext(String next) {
        this.next = next;
    }

    public String getPrev() {
        return this.prev;
    }

    public void setPrev(String prev) {
        this.prev = prev;
    }
}
